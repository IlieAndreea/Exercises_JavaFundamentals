/** Check if a number is odd or even  */

package javafundamentals.statements;

public class JFpdf2Statements6 {
    public static void main(String[] args) {
        int x = 8;
        if (x % 2 == 0) {
            System.out.println(x + " is even");
        } else System.out.println(x + " is odd");
    }
}
